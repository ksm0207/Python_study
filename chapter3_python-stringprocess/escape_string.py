# 탈출문자
# \n : 줄바꿈
print("백문이 불여일견\n백견이 불여일타")


# \" \' : 문장 내에서 따옴표 사용가능
# 저는 김성민 입니다.
print('저는 "김성민"입니다')
print("저는 '김성민'입니다")

# \\ : 문장 내에서 \
print("C:\\Users\\user\\Documents\\GitHub\\Python_study>")

# \r : 커서를 맨 앞으로 이동
print("Red Apple \rPine")  # 커서는 깜빡이는 맨 처음 위치를 뜻함 = Pine Red Apple

# \b : 백스페이스 (한 글자 삭제)
print("Redd\b Apple")

# \t : 탭
print("Red\tApple")
