# 숫자처리함수

print(abs(-5))  # -5에 대한 절대값 = 5

print(pow(4, 2))  # 4^2 = 16

print(max(5, 12))  # 5 와 12사이의 최대값 = 12

print(min(5, 12))  # 최소값 5

print(round(5.15))  # round 반올림 5
print(round(3.99))  # 4

from math import *  # 파이썬의 math 라이브러리 호출 *은 전부 사용하겠다

print(floor(4.99))  # floor 내림 = 4
print(ceil(3.14))  # 올림 4
print(sqrt(16))  # 제곱근 4

