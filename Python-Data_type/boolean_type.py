# Boolean  참 / 거짓

print(5 > 10)  # 거짓 Result = False
print(5 < 10)  # 참   Result = True
print(True)
print(False)

print(not True)  # not은 뒤에 있는 데이터의 반대를 의미함 Result = False
print(not False)  # Result = True
print(not 5 > 10)  # 5는 10보다 크지않은상태(False) not(False의 반대) 이 들어가면 반대상황으로 역전됨 Result = True
